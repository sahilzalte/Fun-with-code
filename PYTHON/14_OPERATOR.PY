# 1. Arithmetic Operators
a = 10
b = 5
print("Arithmetic Operators:")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)

# 2. Relational & Logical Operators
print("\nRelational & Logical Operators:")
print("a > b =", a > b)
print("a < b =", a < b)
print("a == b =", a == b)
print("a != b =", a != b)
print("a > b and b > 0 =", a > b and b > 0)
print("a > b or b > 0 =", a > b or b > 0)

# 3. Assignment Operator
c = a
print("\nAssignment Operator:")
print("c =", c)

# 4. Bitwise Operators
print("\nBitwise Operators:")
print("a & b =", a & b)
print("a | b =", a | b)
print("a ^ b =", a ^ b)
print("~a =", ~a)

# 5. Membership Operators
print("\nMembership Operators:")
list1 = [1, 2, 3, 4, 5]
print("3 in list1 =", 3 in list1)
print("6 not in list1 =", 6 not in list1)

# 6. Identity Operators
print("\nIdentity Operators:")
print("a is c =", a is c)
print("a is not b =", a is not b)
